package Entidades;

/**
 *
 * @author Thiago
 */
public class Plan {
    private String idPlan;
    private String nombre;
    private double costoT;

    public Plan() {
    }

    public Plan(String idPlan, String nombre, double costoT) {
        this.idPlan = idPlan;
        this.nombre = nombre;
        this.costoT = costoT;
    }

    public String getIdPlan() {
        return idPlan;
    }

    public void setIdPlan(String idPlan) {
        this.idPlan = idPlan;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getCostoT() {
        return costoT;
    }

    public void setCostoT(double costoT) {
        this.costoT = costoT;
    }
    
    
}
